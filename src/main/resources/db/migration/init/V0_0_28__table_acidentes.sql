CREATE TABLE smat.Acidentes (
       acidente             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
       area                 int NOT NULL,
       vinculoEmpregaticio  int NOT NULL,
       ocupacao             int NOT NULL,
       localAtendimento     int NULL,
       empregador           int NULL,
       medico               int NULL,
       diagnostico          int NOT NULL,
       fonte                int NOT NULL,
       agenteCausador       int NOT NULL,
       tipoLocalAcidente    int NULL,
       trabalhador          int NOT NULL,
       empregadorTerceiro   int NULL,
       tipoAcidente         int NOT NULL,
       emitente             int NOT NULL,
       municipio            int NOT NULL,
       dataAcidente         timestamp NOT NULL,
       registroPolicial     char(1) NULL,
       amputacao            char(1) NULL,
       obito                char(1) NULL,
       horasTrabalhadas     int NULL,
       descLocalAcidente    varchar(255) NULL,
       numDocFonte          varchar(25) NULL,
       dataEmissaoFonte     timestamp NOT NULL,
       naturezaLesao        varchar(255) NULL,
       dataHoraDiagnostico  timestamp NULL,
       afastamento          char(1) NULL,
       internacao           char(1) NULL,
       observacaoDiagnostico varchar(255) NULL,
       duracaoTratamento    int NULL,
       aposentado           char(1) NULL,
       remuneracao          decimal(8,2) NULL,
       distritoSaude        varchar(10) NULL,
       ultimaTrabalhado     timestamp NULL,
       descricaoSituacaoGeradora varchar(255) NULL,
       descricaoDiagnostico varchar(255) NULL
);